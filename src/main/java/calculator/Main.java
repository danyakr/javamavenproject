package calculator;

/**
 * Класс Main для демонстрации работы калькулятора.
 * 
 * Этот класс служит точкой входа в программу и демонстрирует использование
 * класса {@link Calculator} для выполнения основных арифметических операций.
 * В методе {@link #main(String[])} выполняются примеры сложения, вычитания, 
 * умножения и деления с выводом результатов на консоль.
 */
public class Main {
    
    /**
     * Точка входа в программу. Выполняет демонстрацию основных операций калькулятора.
     *
     * В этом методе создается объект калькулятора и выполняются следующие операции:
     * - сложение двух чисел (5 + 3)
     * - вычитание двух чисел (10 - 2)
     * - умножение двух чисел (4 * 7)
     * - деление двух чисел (8 / 2)
     *
     * Результаты операций выводятся на консоль.
     *
     * @param args Массив строк, содержащий аргументы командной строки (не используется в данном примере).
     */
    public static void main(String[] args) {
        // Создание объекта калькулятора
        Calculator calc = new Calculator();
        
        // Выполнение операций и вывод результатов на консоль
        System.out.println("5 + 3 = " + calc.add(5, 3));
        System.out.println("10 - 2 = " + calc.subtract(10, 2));
        System.out.println("4 * 7 = " + calc.multiply(4, 7));
        System.out.println("8 / 2 = " + calc.divide(8, 2));
    }
}

